int		ft_atoi(char *str)
{
	int i;
	int n;
	int r;

	i = 0;
	n = 1;
	r = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	i--;
	while (str[++i] == '-' || str[i] == '+')
		if (str[i] == '-')
			n *= -1;
	while (str[i] != '\0')
	{
		while (str[i] >= 48 && str[i] <= 57)
		{
			r = r * 10 + str[i] - 48;
			i++;
		}
		return (n * r);
	}
	return (0);
}
