int	ft_sqrt(int nb)
{
	int nombre;
	int i;
	int compt;

	i = 0;
	compt = nb / 2;
	nombre = 0;
	while (i <= compt)
	{
		nombre = i * i;
		if (nombre == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
